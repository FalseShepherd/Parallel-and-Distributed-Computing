#ifndef ParallelMergeSort_h
#define ParallelMergeSort_h

#include <vector>
#include <cstddef>

// MergeSort
//
// @param a - vector to be sorted
// @param tmp - vector of the same size as a, used for temporary space
// @param first - index of first entry in range to be sorted
// @param last - index (inclusive) of last entry in range to be sorted
void ParallelMergeSort(std::vector<double> &a,
	std::vector<double> &tmp,
	size_t first,
	size_t last);
void PMergeSort(std::vector<double> &a,
	std::vector<double> &tmp,
	size_t first,
	size_t last);

#endif //  ParallelMergeSort_h
